public class Fox {

  String name, type, color;

  public Fox(String name, String type, String color) {
    this.name = name;
    this.type = type;
    this.color = color;
  }

  public String getType() {
    return type;
  }

  public String getColor() {
    return color;
  }

  public String getName() {
    return name;
  }
}
